
INTERSECTION CRITERIA
=====================

	All Intersection table maintenance	  is automated
	Street & Centerline table maintenance is manual
		(See \\ 01_Extract_Intersection.sql for exceptions)
	
	All possible intersection types must be generated
		(See \\ Build_Intersection_Package for descriptions)

	Duplicate intersections    are forbidden

	Duplicate intersection IDs are forbidden

	An Intersection ID for a given intersection must never change

	All Intersection region sets must be accurate 

	All street & centerline problems must be detected & corrected


