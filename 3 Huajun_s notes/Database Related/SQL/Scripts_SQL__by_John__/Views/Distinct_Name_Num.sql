

		SELECT	DISTINCT
				A.ADDR_TYPE,
				A.ADDR_PFX,
				A.ADDR_SFX
		FROM		CDRELADR		A
		WHERE	(
				A.ADDR_NAME,
				A.ADDR_NUM	
			)	
		IN
			(
			SELECT 	X.ADDR_NAME,
					X.ADDR_NUM
			FROM		CDRELADR	X

			WHERE		X.ADDR_NAME = U.ADDR_NAME		
			AND		X.ADDR_NUM 	= U.ADDR_NUM		

			GROUP BY 	X.ADDR_NAME,
					X.ADDR_NUM
			HAVING 	COUNT (DISTINCT X.ADDR_TYPE || X.ADDR_PFX || X.ADDR_SFX ) = 1 
			)
	;






